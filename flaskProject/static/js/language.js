function changeLanguage(lang) {
    // Save selected language to localStorage
    localStorage.setItem('selectedLanguage', lang);

    // Update flag and language text
    const flagMap = {
        'bg': 'https://flagcdn.com/w20/bg.png',
        'en': 'https://flagcdn.com/w20/gb.png',
        'ru': 'https://flagcdn.com/w20/ru.png'
    };

    const langTextMap = {
        'bg': '–ë—ä–ª–≥–∞—Ä—Å–∫–∏',
        'en': 'English',
        'ru': '–†—É—Å—Å–∫–∏–π'
    };

    document.getElementById('selected-flag').src = flagMap[lang];
    document.getElementById('selected-lang').textContent = langTextMap[lang];

    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' && element.placeholder) {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update dropdown options selected state
    document.querySelectorAll('#language-options div').forEach(option => {
        option.setAttribute('aria-selected', option.getAttribute('data-lang') === lang);
    });
    document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang][key]) {
        if (element.tagName === 'INPUT' && element.placeholder) {
            element.placeholder = translations[lang][key];  // –ó–∞ placeholder
        } else if (element.tagName === 'INPUT' || element.tagName === 'BUTTON') {
            element.value = translations[lang][key];  // –ó–∞ —Å—Ç–æ–π–Ω–æ—Å—Ç–∏ –Ω–∞ –±—É—Ç–æ–Ω–∏/–∏–Ω–ø—É—Ç–∏
        } else {
            element.textContent = translations[lang][key];  // –ó–∞ –æ–±–∏–∫–Ω–æ–≤–µ–Ω —Ç–µ–∫—Å—Ç
        }
    }
});
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('selectedLanguage') || 'bg';
    changeLanguage(savedLang);

    // Handle language dropdown click
    document.getElementById('language-dropdown').addEventListener('click', function(e) {
        e.stopPropagation();
        document.getElementById('language-options').classList.toggle('show');
    });

    // Handle language selection
    document.querySelectorAll('#language-options div').forEach(option => {
        option.addEventListener('click', function() {
            const lang = this.getAttribute('data-lang');
            changeLanguage(lang);
            document.getElementById('language-options').classList.remove('show');
        });
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', function() {
        document.getElementById('language-options').classList.remove('show');
    });
});